    public ListaMovsExistentes(Vector<Movimiento> movs) {
        initComponents();
        String datos[][]={};
        String cabecera[]={"Fecha","Tipo","Origen","Destino","Nota"};
        DefaultTableModel md = new DefaultTableModel(datos,cabecera);        
        for (int i = 0; i < movs.size(); i++) {
            Vector fila = new Vector();
            fila.add(movs.elementAt(i).getFecha());
            fila.add(quitaGuion(movs.elementAt(i).getTipo()));
            fila.add(quitaGuion(movs.elementAt(i).getOrigen()));
            fila.add(quitaGuion(movs.elementAt(i).getDestino()));
            fila.add(quitaGuion(movs.elementAt(i).getNota()));
            md.addRow(fila);            
        }
        tabla.setModel(md); //asigna el modelo        
    }

