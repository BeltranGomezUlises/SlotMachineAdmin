    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int index = tabla.getSelectedRow();
        if (index != -1) {
            if (JOptionPane.showConfirmDialog(null, "¿Seguro de eliminar la etiqueta \"" +quitaGuion(etiquetas.get(index).getNombre()) + "\"?", "eliminar", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE) == JOptionPane.YES_OPTION) {
                etiquetas.remove(index);
                Etiqueta.actualizarVD(etiquetas);
                DefaultTableModel md = (DefaultTableModel)  tabla.getModel();
                md.removeRow(index);                
            }
        }else{
            JOptionPane.showMessageDialog(null, "Debe seleccionar una etiqueta", "Atención", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

